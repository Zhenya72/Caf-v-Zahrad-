function applyTranslations(lang) {
  const dict = translations[lang] || {};

  document.querySelectorAll('[data-lang]').forEach(el => {
    const key = el.dataset.lang;
    if (key && (key in dict)) {
      el.innerHTML = dict[key];
    }
  });

  document.querySelectorAll('[data-lang-placeholder]').forEach(el => {
    const key = el.dataset.langPlaceholder;
    if (key && (key in dict)) {
      el.placeholder = dict[key];
    }
  });

  document.querySelectorAll('option[data-lang]').forEach(opt => {
    const key = opt.dataset.lang;
    if (key && (key in dict)) {
      opt.textContent = dict[key];
    }
  });

  document.querySelectorAll('[data-lang-alt]').forEach(img => {
    const key = img.dataset.langAlt;
    if (key && (key in dict)) {
      img.alt = dict[key];
    }
  });

  document.querySelectorAll('.lang-btn').forEach(btn => btn.classList.remove('active'));
  const activeBtn = document.querySelector(`.lang-btn[data-lang-btn="${lang}"]`);
  if (activeBtn) activeBtn.classList.add('active');
}

function switchLang(lang) {
  localStorage.setItem('lang', lang);
  applyTranslations(lang);
}

document.addEventListener('DOMContentLoaded', () => {
  const saved = localStorage.getItem('lang') || 'cz';
  applyTranslations(saved);

  document.querySelectorAll('.lang-btn').forEach(btn => {
    btn.addEventListener('click', () => {
      const lang = btn.dataset.langBtn;
      if (lang) switchLang(lang);
    });
  });
});